// Funci√≥n para esperar a que adminPanel est√© disponible
function waitForAdminPanel() {
    return new Promise((resolve) => {
        const checkPanel = () => {
            if (window.adminPanel && typeof window.adminPanel.createArticle === 'function') {
                console.log('‚úÖ adminPanel encontrado y funcional:', window.adminPanel);
                resolve(window.adminPanel);
            } else {
                console.log('‚è≥ Esperando adminPanel funcional...');
                setTimeout(checkPanel, 200);
            }
        };
        checkPanel();
    });
}

